class teamMember {
    constructor(name, id, email, number) {
        this.name = name;
        this.id = id;
        this.email = email;
        this.number = number;
    }
    Name() {
        return this.name;
    }
    Id() {
        return this.id;
    }
    Email() {
        return this.email;
    }
    Number() {
        return this.number;
    }
}